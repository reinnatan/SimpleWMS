package com.simple.serializer;

public class WarehouseJSON {
	private String name;
	private String address;
	private String phone;
	private float longWarehouse;
	private float widthWarehouse;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public float getLongWarehouse() {
		return longWarehouse;
	}
	public void setLongWarehouse(float longWarehouse) {
		this.longWarehouse = longWarehouse;
	}
	public float getWidthWarehouse() {
		return widthWarehouse;
	}
	public void setWidthWarehouse(float widthWarehouse) {
		this.widthWarehouse = widthWarehouse;
	}
	
}
